package variable;

public class Var5 {

    public static void main(String[] args) {
        int a; // 변수 선언
        a = 1; // 변수 초기화
        System.out.println(a);

        int b = 2; // 변수 선언, 초기화 한번에
        System.out.println(b);

        int c = 3, d = 4; // 여러 변수 선언, 초기화 한번에
        System.out.println(c);
        System.out.println(d);
    }
}
