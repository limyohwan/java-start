package variable;

public class Var7 {

    public static void main(String[] args) { // 타입 = 형식 = 형
        int a = 100; // 정수
        double b = 10.5; // 실수
        boolean c = true; // 불리언 true, false
        char d = 'A'; // 문자 하나
        String e = "Hello Java"; // 문자열, 문자열을 다루기 위한 특별한 타입
        // 100, 10.5, true, 'A', "Hello Java" 를 리터럴(literal)이라고 함 = 개발자가 직접 입력한 고정된 값

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
